<h3>Code of Conduct</h3><br>
<ol type="A">
   
    <li><strong>During Flag Ceremony/Morning Praise</strong><br>
        Flag ceremonies are held every Monday and Friday at 6:45 am at the Quadrangle and Covered Court.
        <ol>
            <li>You are required to attend the regular flag ceremony at the designated area even before the first bell at 7: 10 am.</li>
            <li>You should assemble as a section and form two lines, one for the girls and one for the boys. An arms-length distance should be kept from each student.</li>
            <li>You should sing the National Anthem and recite the Pledge of Allegiance properly</li>
        </ol>
    </li>
    
    <li><strong>School Campus</strong><br>
        St. Joseph’s School of Mactan is your school and second home. You are expected to observe the following:
        <ol type="1">
            <li>Keep the premises clean at all times</li>
            <li>Use the trash receptacles properly</li>
            <li>Avoid unnecessary loud voice while classes are going on</li>
        </ol>
    </li>
    
    <li><strong>Prayer Room</strong><br>
        The Prayer Room is a place for prayer:
        <ol type="1">
            <li>Maintain its prayerful atmosphere at all times.</li>
            <li>Take a posture that promotes prayerfulness</li>
            <li>Silence is to be observed.</li>
        </ol>
    </li>
    
    <li><strong>Library</strong><br>
        You are expected to observe the following in the use of the Library facilities.               
        <ol type="1">
            <li>Handle books and other materials with care. Any willful and deliberate damage done to library materials or facilities is considered a serious offense and may mean the loss of one’s library privilege.</li>
            <li>Bags, knapsacks, etc. should be left outside the library.</li>
            <li>Books should be returned to the shelves to avoid misplacement.</li>
            <li>All books should be borrowed from the librarian or staff of the charging counter.</li>
            <li>All reading materials must be checked by the library staff before they are taken out of the library.</li>
            <li>Return borrowed materials promptly. Books should be returned within seven (7) days.</li>
            <li>General reference books, magazines and newspaper are not to be taken out of the library.</li>
            <li>The loss or damage to any book must be immediately reported.</li>
            <li>Fines are imposed for overdue books per day including Sundays and Holidays.</li>
        </ol>        
    </li>
    
    <li><strong>Audio-Visual Materials</strong><br>
        <ol type="1">
            <li>When borrowing audio-visual materials and equipment, secure a request form from the Property Custodian</li>
            <li>The Borrower’s slip is signed by the borrower and countersigned by the Teacher concerned</li>
            <li>The borrower is responsible for the borrowed equipment so he/she answers for any damage or loss thereof.</li>
            <li>Any borrowed material should be returned at the end of the day or immediately after use. </li>
        </ol>        
    </li>
    
    <li><strong>Clinic</strong><br>
A learner who feels sick may go to the Clinic, but first seeks the permission of the subject teacher. The pupil/student is excused from the class because of illness.

When the learner is advised to go home, the parents or guardians will be informed and will be asked to fetch their child. A duly signed permission slip to leave the school is issued by the health officer.<br><br>   
    </li>
    
    <li><strong>Science and T.H.E., Computer Laboratories</strong><br>
The laboratories are for practicum purposes only. Learners are expected to care for and use the equipment properly. Light and water, air-con units should be put off when not in use.<br><br>    
    </li>
    
    <li><strong>Canteen</strong><br>
Everyone should maintain an orderly and clean canteen and dining area for learners and members of the school community. Everyone should treat each other with courtesy.
        <ol type="1">
            <li>Line up when buying food. Keep hands, feet and objects to yourself when in line</li>
            <li>Give way. Boys should give way to girls, and intermediate students should give way to primary children when having the same turn in the line to buy food.</li>
            <li>Observe order. You should avoid running or playing in the canteen.</li>
            <li>Observe good manners. You should eat quietly and observe good table manners.</li>
            <li>Clean up after eating. All trash should be disposed of properly.</li>
        </ol>       
    </li>
    
    <li><strong>Athletic Equipments</strong><br>
Athletic equipments have to be handled with care. The borrower is also responsible for returning the equipment after use. Any damage should be reported at once and will be charged to the borrower’s account.<br><br>
    </li>
    
    <li><strong>Lost/Worn Out IDs</strong><br>
        <ol type="1">
            <li>Your ID is part of your school uniform. You must wear it with an ID holder and an official SJSM sling while you are in school. ID’s placed inside the pocket or wallet or not worn properly will be confiscated.</li>
            <li>If you lose your ID, you should:
                <ol type="1">
                    <li>Present to the OSA an excuse letter from your parents/guardians informing that you’re ID had been lost.</li>
                    <li>Pay a replacement fee at the Cashier’s Office.</li>
                    <li>Tampering of school ID and its fraudulent use, i.e. , lending or borrowing, is subject to disciplinary sanction.  </li>
                </ol>
            </li>
        </ol>    
    </li>
    
    <li><strong>Comfort Rooms</strong><br>
        <ol type="1">
            <li>Use the C.R Pass when going to the comfort room.</li>
            <li>Flush the toilet after use.</li>
            <li>Dispose paper towel/tissue proper in the trash can</li>
            <li>Wash your hands with soap after using the comfort room.</li>
            <li>Keep the walls free from writing.</li>
            <li>Leave the place presentable for the next person to use.</li>
        </ol>    
    </li>
    
    <li><strong>Use of Mobile Phones in the School</strong><br><br>
You are to behave responsibly in the use of communication and information gadgets/equipments, especially when engaging in any action that may put impact on the privacy, dignity, and/or rights of institutions, groups or individuals. If you decide to bring a cell phone to school, you do so at your own risk, the following policies will apply:
        <ol type="1">
            <li>The cellular phone must be switched off or placed in silent mode during class hours to avoid disruption to the normal routine of the class activities, and must only be used before and after classes.</li>
            <li>Violation of the above provision would warrant the following sanctions:
                <ul>
                    <li><strong>First Offense</strong><br>
The cell phone will be confiscated by the teacher and will be surrendered to the OSA. The student will be able to collect the cell phone at the end of the school day and a record of the incident will be made. A violation note will be sent to the parent/ guardian to inform them of the incident.                    
                    </li>
                    <li><strong>Second Offense</strong><br>
The cell phone will be confiscated by the teacher and will be surrendered to the OSA. The incident will be recorded. Parents/Guardians will be notified and the student can only collect the phone after the parent/guardian has given verbal consent to the OSA for the student to collect the phone.                     
                    </li>
                    <li><strong>Third Offense</strong><br>
The cell phone will be confiscated by the teacher and will be surrendered to the OSA. Parents will be notified to personally retrieve the phone.
                    </li>
                    <li><strong>Fourth Offense</strong><br>
The school will withdraw the agreement of allowing the student to bring the cellular phone to school.                    
                    </li>
                </ul>
            </li>
            <li>The Bluetooth function of the mobile phone must be switched off at all times and not to be used to send images or files to other mobile phones.</li>
            <li>The use of the mobile phone to cheat in exams is absolutely forbidden.</li>
            <li>Using mobile phones to bully and threaten other learners 
is unacceptable and will not be tolerated. It is forbidden to use the mobile phone to take videos and pictures of acts to denigrate and/ or humiliate a learner or a teacher and then send the pictures to other learners or upload it to a website for public viewing. This also includes using mobile phones to photograph or video 
any learner or teacher without their consent. Using
 mobile phones to menace, harass or offend another person is a criminal offense.
            </li>
            <li>Mobile phones are not to be used or taken into changing rooms or toilet or used in any situation that may cause embarrassment or discomfort to fellow learners, staff or visitors to the school.</li>
            <li>Violation of provisions 3 to 6 would warrant stern disciplinary action.</li>
            <li>Student/pupil is bringing his/her cellular phone at his/her own risk. Therefore, the school bears no responsibility for the lost, stolen or damaged cellular phone under his/her care.</li>
            <li>To reduce the risk of the theft during school hours, learners who carry cell phones are advised to keep them well concealed.</li>
            <li>Parents/Guardians are reminded that in cases of emergency, the OSA remains a vital and appropriate point of contact and can ensure that their child is reached quickly and assisted in any appropriate way.</li>
        </ol>    
    </li>
    
    <li><strong>Other Gadgets such as, but not Limited to PSP, IPod,   DS,  Laptop and Tablet</strong><br>
Other gadgets such as, but not limited to PSP, IPOD, DS, LAPTOP and TABLET are not allowed in school unless a written permission is obtained from the OSA.<br><br>    
    </li>
    
    <li><strong>Curfew</strong><br>
        <ol type="1">
            <li>Observe school curfew time. One is advised to go to the waiting area immediately after dismissal. One is not allowed to play or loiter in the campus past curfew hours.</li>
            <li>If a learner wishes to stay in campus beyond curfew hours to practice or for other school-related activities, </li>
            <li>He/she should fill up a parents’ permit slip in the communication notebook, have it signed by his/her parents and adviser, and presented it to the OSA for approval.</li>
        </ol>    
    </li>
    
    <li><strong>Off-limit Zones</strong><br>
The following are considered restricted areas to BED students:
        <ol>
            <li>On Campus
                <ol type="a">
                    <li>Sisters’ Convent</li>
                    <li>Faculty Room</li>
                    <li>Guard’s house</li>
                </ol>
            </li>
        </ol>    
    </li>
    
    <li><strong>Care and Cleanliness of the School Grounds</strong><br>
As a member of the school community, you are expected to help implement the 5 M program which aims to make the school <em>malinis, maayos, maganda, mabango, at maaliwalas</em> .It is therefore part of the student’s responsibility to remind or to correct those who forget to observe rules of cleanliness. You should pick up litter without having to be told and should be put in the trash receptacles provided for this purpose.    
    </li>
    
    <li><strong>Care and Responsible Use of School Property</strong><br>
All are expected to take pride in our school and show it by treating school properties/facilities with care, keeping the campus and buildings neat and clean.
        <ol type="1">
            <li>Any damage done to school property or the property of others will be repaired at the culprit’s expense and will subject him/her to disciplinary sanction.</li>
            <li>Damage property such as broken window panes scratched desks and chairs, etc. should be reported at the Office of the Physical Plant.</li>
            <li>Sitting on top of tables, chair’s backrest, sliding down the stair, railways, banging, kicking of blackboards, bulletin boards, etc. will be subject to disciplinary sanctions.</li>
            <li>Sitting on the ledge is not allowed.</li>
            <li>Playing in the comfort rooms, pushing, wasting water, or committing any acts of vandalism will be subject to disciplinary sanctions.</li>
        </ol>       
    </li>
    
    <li><strong>Responsibility Over Personal Belongings</strong><br>
        <ol type="1">
            <li>Learners are expected to bring all school materials needed for the day. One is not allowed to phone home to ask somebody to bring forgotten materials, such as notebooks, books, projects, athletic uniform, etc.</li>
            <li>The receipt and delivery of left items at home to school is not allowed.</li>
            <li>All are strongly discouraged from bringing and/or wearing expensive personal effects such as jewelry and watches to school. Carrying big amounts of money to school is also discouraged. This is to avoid sources of temptation for other.</li>
            <li>Expensive items brought to school will be confiscated and parents will be required to personally redeem these items from the OSA. All are responsible for their personal belongings hence, all are discouraged from leaving one’s belongings lying around the school campus unattended
</li>
        </ol>    
    </li>
    
    <li><strong>Prohibited Materials</strong><br>
To avoid vandalism and other untoward incidents, one is not allowed to bring the following materials to school:
        <ol type="1">
            <li>Paint spray</li>
            <li>Knife or other sharp-pointed objects</li>
            <li>Pellet guns</li>
            <li>Any kind of flammable, hazardous or explosive material</li>
            <li>Morally corrupting materials (such as gambling materials, pornographic items and the like)</li>
        </ol>    
    </li>
    
    <li><strong>Lost and Found</strong><br>
        <ol type="1">
            <li>All are responsible for keeping their personal belongings safe and secure. In case of loss, report the incident to the class adviser the soonest possible time. The school shall not be held liable for any losses because of negligence, non-compliance, etc.</li>
            <li>Lost and found items are be brought to the Office of the Student Affairs, shall record the item found, where it was found, and the name of the person who found it.</li>
            <li>Items not claimed after one (1) week will be donated to the BED Student Council to be used for outreach activities.</li>
        </ol>    
    </li>
</ol>
