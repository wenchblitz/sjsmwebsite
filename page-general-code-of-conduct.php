<h3>School Attendance</h3><br>
<ol type="A">
    <li><strong>Absence from School</strong>
        <ol type="1">
            <li>All learners are to attend class regularly and punctually. If one will be absent from class on a certain date, he/she must present to the Officer of Student Discipline (OSA) a letter from his/her parents or guardian stating the reason why he/she intends, to be absent from class.</li>            
            <li>Upon his/her return to school, parents/guardian have to fill out the excuse letter form in the Communication notebook. This excuse letter must be presented to the OSA.</li>            
            <li>If one has been absent from class due to an illness he/she should submit a medical certificate along with the excuse letter to the OSA. Students/pupils who have been sick with communicable diseases must first secure the clearance of the school physician or nurse before being allowed to attend classes.</li>            
            <li>One is held responsible for all assignments and for the entire content of the subjects missed during his/her absence. Absences do not exempt one from academic requirements like projects, reports, themes and the like.</li>            
            <li>Suspended learners are marked absent during the period of their suspension. Suspension is a penalty in which the school is allowed to deny or to deprive an erring student of attendance in classes for a certain period.</li>            
            <li>Habitual and persistent absence is a ground for non- admission the following school year.</li>            
            <li>Learners who incur more than the required number of absences (20% of the total number of school days) shall be dropped.</li>            
            <li>Class days lost due to late registration shall be considered absences.</li>            
            <li>A learner who has been absent for three (3) days has to accompanied by a parent or a guardian to explain the reason for the incurred absences of the student.</li>            
            <li>A learner who has been absent for ten (10) consecutive days without prior notice shall be dropped.</li>            
            <li>Absence not authorized by the parents/ guardians is considered truancy.</li>                       
        </ol>
    </li>
    <li><strong>Tardiness</strong>
        <ol type="1">
            <li>One is considered late for class if he/she is not inside the classroom after the daily flag ceremony.at 7:15 am, and if 
one come after the signal bell for class after recess and lunch.</li>
            <li>An accumulation of five (5) tardy marks shall be equivalent to one day absence. </li>
            <li>Three (3) tardy marks would require the learner to bring his/her parent for conference with the OSA.</li>
            <li>Students who are habitually late to class without any valid reason may not be admitted the following school year. Traffic problems and waking up late are not valid reasons for tardiness.</li>
            <li>Learners who are late for class in any of the above mentioned circumstances and in between periods will not be admitted to class without the permit slip from the OSA.</li>
        </ol>
    </li>
    <li><strong>Permission to leave School</strong>
        <ol type="1">
            <li>One is not allowed to leave the school premises without written permission from the OSA.</li>
            <li>One may leave school earlier than dismissal time if he/she presents a letter to the OSA signed by his/her parents or guardians requesting for an early dismissal.</li>
            <li>A gate pass, properly filled up and signed by the OSA must be presented to the guard before leaving.</li>
        </ol> 
    </li>
    <li><strong>Suspension of Classes</strong>
        <ol type="1">
            <li>Classes are automatically suspended when typhoon signal no. 2 is raised by the Philippine Atmospheric Geophysical and Astronomical Services Administration (PAGASA. PAGASA normally makes the announcement over broadcast media 11:00 in the evening and 5:00 in the morning.</li>
            <li>If the announcement is made at the time you are already in school, classes will still have to go on to avoid disorder; however your parents/guardians may get a gate pass from the OSA for one to be allowed to go home.</li>
            <li>Localized suspension of classes may be made by the School Directress, Division Superintendent, or Local Government Executive when there are threats to public health and safety, such as heavy rains earthquakes, floods, and transport strikes.</li>
            <li>Parents/guardians may or may not send there to school during inclement weather conditions. It is the ultimate responsibility of the parents/guardians to decide on this matter.</li>
        </ol>  
    </li>
    <li><strong>Call/ Appointment</strong>
        <ol type="1">
            <li>A learner called from class by the Principal, Officer of Student Affairs or Guidance Counselor is not considered absent from class.</li>
            <li>A learner who is taking a test may not be called out of class unless the slip is marked “Urgent”.</li>
        </ol>
    </li>
</ol>

